#DROP TABLE IF EXISTS Bookings;
#DROP TABLE IF EXISTS Artists;

CREATE TABLE IF NOT EXISTS Artists
(
  ID   INT NOT NULL AUTO_INCREMENT,
  Name VARCHAR(20),
  Fee  DOUBLE,
  PRIMARY KEY (ID)
);

CREATE TABLE IF NOT EXISTS Bookings
(
  ID INT NOT NULL AUTO_INCREMENT,
  Name VARCHAR(20),
  Date DATE,
  StartTime TIME,
  FinishTime TIME,
  PRIMARY KEY (ID)
);